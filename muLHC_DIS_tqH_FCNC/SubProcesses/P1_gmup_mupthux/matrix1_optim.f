      DATA (NHEL(I,25),I=0,6) /25, 1, 1, 1,-1, 0, 1/
      DATA (NHEL(I,26),I=0,6) /26, 1, 1, 1,-1, 0,-1/
      DATA (NHEL(I,27),I=0,6) /27, 1, 1, 1, 1, 0, 1/
      DATA (NHEL(I,28),I=0,6) /28, 1, 1, 1, 1, 0,-1/
      DATA (NHEL(I,29),I=0,6) /29, 1, 1,-1,-1, 0, 1/
      DATA (NHEL(I,30),I=0,6) /30, 1, 1,-1,-1, 0,-1/
      DATA (NHEL(I,31),I=0,6) /31, 1, 1,-1, 1, 0, 1/
